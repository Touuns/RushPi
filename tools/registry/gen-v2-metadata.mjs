#!/usr/bin/env node
// Authoring tool (run once when the selection changes): copies the curated
// selection input into data/v2-metadata.mjs, the committed source of truth the
// build reads.
//
// 12C-1B1.1 hardening: tokenIds are AUTHOR-ASSIGNED. Every entry in
// data/v2-selection-input.mjs already carries an explicit literal tokenId; this
// generator COPIES e.tokenId verbatim and NEVER allocates one (no FIRST_NEW_ID,
// no index arithmetic). It fails on a missing / malformed / duplicate tokenId
// or any collision with the frozen V1 range, so reordering or inserting a
// selection can never renumber another asset. Adding an asset requires manually
// choosing an unused rpt-#### id in the input.
import { readFileSync, writeFileSync } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";
import { V2_NEW_SELECTIONS } from "./data/v2-selection-input.mjs";
import { validateSelectionInput } from "./lib/validateV2.mjs";

const here = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(here, "../..");
const outPath = path.join(here, "data", "v2-metadata.mjs");

function frozenV1TokenIds() {
  const v1 = JSON.parse(readFileSync(path.join(repoRoot, "registry/tokens/v1/registry.json"), "utf8"));
  return new Set(v1.entries.map((e) => e.tokenId));
}

function serializeEntry(e) {
  const parts = [
    `tokenId: ${JSON.stringify(e.tokenId)}`, // copied verbatim from the input
    `id: ${JSON.stringify(e.id)}`,
    `name: ${JSON.stringify(e.name)}`,
    `symbol: ${JSON.stringify(e.symbol)}`,
    `slug: ${JSON.stringify(e.slug)}`,
    `category: ${JSON.stringify(e.category)}`,
    `assetClass: ${JSON.stringify(e.assetClass)}`,
    `eligibilityTier: ${JSON.stringify(e.tier)}`,
    `aliases: ${JSON.stringify(e.aliases ?? [])}`,
  ];
  if (e.symbolConflictGroup) parts.push(`symbolConflictGroup: ${JSON.stringify(e.symbolConflictGroup)}`);
  return `  { ${parts.join(", ")} },`;
}

function main() {
  const errors = validateSelectionInput(V2_NEW_SELECTIONS, frozenV1TokenIds());
  if (errors.length > 0) {
    console.error(`Selection input INVALID (${errors.length} error(s)) — refusing to generate:`);
    for (const e of errors) console.error(` - ${e}`);
    process.exit(1);
  }

  // Emit in tokenId order for a stable, reviewable file. Ordering here is
  // presentational only: each tokenId is copied from the input, never derived.
  const ordered = V2_NEW_SELECTIONS.slice().sort((a, b) => (a.tokenId < b.tokenId ? -1 : a.tokenId > b.tokenId ? 1 : 0));
  const lines = ordered.map(serializeEntry);
  const header = `// GENERATED by tools/registry/gen-v2-metadata.mjs from
// data/v2-selection-input.mjs. Do not hand-edit: change the input then
// re-run \`node tools/registry/gen-v2-metadata.mjs\`.
//
// Committed source of truth for the 214 NEW V2 proposal entries. Every tokenId
// below is an AUTHOR-ASSIGNED literal COPIED from the input — the generator
// never allocates a tokenId from array position. The 36 frozen V1 entries
// (rpt-0001..rpt-0036) are preserved verbatim by the build from
// registry/tokens/v1/registry.json and are not listed here.
export const V2_NEW_ENTRIES = [
`;
  writeFileSync(outPath, `${header}${lines.join("\n")}\n];\n`, "utf8");
  console.log(`Wrote ${outPath} with ${ordered.length} entries (tokenIds copied verbatim from the input).`);
}

main();
